# Operadores aritméticos
a = 10
b = 3

suma = a + b         # Suma
resta = a - b        # Resta
multiplicacion = a * b  # Multiplicación
division = a / b     # División
division_entera = a // b  # División entera
modulo = a % b       # Módulo (resto de la división)
exponente = a ** b   # Exponente

print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("División Entera:", division_entera)
print("Módulo:", modulo)
print("Exponente:", exponente)

# Operadores de comparación
es_igual = (a == b)  # Igualdad
es_diferente = (a != b)  # Desigualdad
mayor_que = (a > b)  # Mayor que
menor_que = (a < b)  # Menor que
mayor_o_igual = (a >= b)  # Mayor o igual que
menor_o_igual = (a <= b)  # Menor o igual que

print("Es igual:", es_igual)
print("Es diferente:", es_diferente)
print("Mayor que:", mayor_que)
print("Menor que:", menor_que)
print("Mayor o igual que:", mayor_o_igual)
print("Menor o igual que:", menor_o_igual)
