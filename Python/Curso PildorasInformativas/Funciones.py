# Definir una función
def saludar(nombre):
    """Esta función recibe un nombre y lo imprime con un saludo."""
    print("Hola,", nombre)

# Llamar a la función
saludar("Juan")

# Función que suma dos números
def sumar(a, b):
    """Esta función recibe dos números y retorna su suma."""
    return a + b

# Llamar a la función y almacenar el resultado
resultado = sumar(5, 3)
print("La suma es:", resultado)
